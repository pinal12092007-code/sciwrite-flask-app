def call_gemini_with_retry(api_key: str, prompt: str, retries: int = 3) -> str:
    client = genai.Client(api_key=api_key)
    for attempt in range(retries):
        try:
            res = client.models.generate_content(
                model=GEMINI_MODEL, contents=prompt,
                config=genai.types.GenerateContentConfig(temperature=0.2)
            )
            return res.text
        except Exception as e:
            if "503" in str(e) or "429" in str(e):
                if attempt < retries - 1:
                    time.sleep(2 ** attempt)
                    continue
            raise e
    return ""

def clean_llm_latex_output(raw_output: str) -> str:
    clean_text = raw_output.strip()
    if clean_text.startswith("```"):
        clean_text = re.sub(r'^```[a-zA-Z]*\n', '', clean_text)
        clean_text = re.sub(r'\n```$', '', clean_text)
    return clean_text.strip()

def generate_academic_chart(api_key: str, paper_title: str, results_notes: str, figure_index: int) -> tuple[str, bytes]:
    client = genai.Client(api_key=api_key)
    # Use /tmp for serverless compatibility
    chart_filename = f"/tmp/generated_chart_{figure_index}.png"
    base_name = f"generated_chart_{figure_index}.png"
    
    prompt = f"""
    You are an expert Data Scientist. Write an isolated Python script using matplotlib to generate a chart.
    Title: {paper_title}
    Data: {results_notes}
    Save Path: '{chart_filename}'
    CRITICAL: Output ONLY raw python code. Use plt.savefig('{chart_filename}', dpi=300, bbox_inches='tight'). Use plt.close('all').
    """
    try:
        res = client.models.generate_content(model=GEMINI_MODEL, contents=prompt,
            config=genai.types.GenerateContentConfig(temperature=0.1))
        clean_code = res.text.strip().replace("```python", "").replace("```", "")
        
        local_scope = {"plt": plt, "pd": pd, "re": re}
        plt.close('all')
        exec(clean_code, globals(), local_scope)
        
        if os.path.exists(chart_filename):
            with open(chart_filename, "rb") as f:
                img_bytes = f.read()
            os.remove(chart_filename)
            plt.close('all')
            return base_name, img_bytes
    except Exception:
        plt.close('all')
    return None

def make_jinja_env() -> jinja2.Environment:
    return jinja2.Environment(
        block_start_string="[%", block_end_string="%]",
        variable_start_string="[[", variable_end_string="]]",
        comment_start_string="[#", comment_end_string="#]",
        trim_blocks=True, lstrip_blocks=True, autoescape=False
    )

def render_latex(title, authors_list, two_col, keywords, sections, bibtex, figs=None):
    env = make_jinja_env()
    tmpl = env.from_string(LATEX_TMPL)
    escaped_authors = []
    first_author = ""
    for a in authors_list:
        if not a.get("name"): continue
        if not first_author: first_author = escape_for_latex(a["name"])
        escaped_authors.append({
            "name": escape_for_latex(a["name"]),
            "affil": escape_for_latex(a.get("affil", "")),
            "email": escape_for_latex(a.get("email", ""))
        })
    short = (title[:52]+"...") if len(title) > 55 else title
    return tmpl.render(
        escaped_title=escape_for_latex(title), short_title=escape_for_latex(short),
        first_author=first_author, escaped_kw=escape_for_latex(keywords), kw_raw=keywords.strip(),
        authors=escaped_authors, two_col=two_col, sec=sections, bibtex=bibtex.strip(), figs=figs or []
    )

def generate_overleaf_zip(latex_src: str, bibtex_data: str, uploaded_figs: list, generated_figs: list) -> bytes:
    zip_buffer = io.BytesIO()
    with zipfile.ZipFile(zip_buffer, "w", zipfile.ZIP_DEFLATED) as zip_file:
        zip_file.writestr("main.tex", latex_src)
        if bibtex_data.strip():
            zip_file.writestr("references.bib", bibtex_data.strip())
        for uf in uploaded_figs:
            safe_name = re.sub(r"[^\w.\-]", "_", uf['filename'])
            zip_file.writestr(safe_name, uf['data'])
        for filename, img_bytes in generated_figs:
            zip_file.writestr(filename, img_bytes)
    return zip_buffer.getvalue()

