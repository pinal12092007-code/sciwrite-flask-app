@app.route('/api/generate', methods=['POST'])
def generate():
    api_key = os.getenv("GEMINI_API_KEY")
    if not api_key:
        return jsonify({"error": "GEMINI_API_KEY environment variable not set on server."}), 500

    try:
        # Parse text fields
        title = request.form.get('title', '')
        keywords = request.form.get('keywords', '')
        authors_json = request.form.get('authors', '[]')
        authors_list = json.loads(authors_json)
        
        abstract_goals = request.form.get('abstract_goals', '')
        intro_bg = request.form.get('intro_bg', '')
        methodology_notes = request.form.get('methodology_notes', '')
        results_data = request.form.get('results_data', '')
        extra_notes = request.form.get('extra_notes', '')
        bibtex_entries = request.form.get('bibtex_entries', '')
        
        layout_opt = request.form.get('layout_opt', 'Single Column')
        two_col = layout_opt == 'Double Column'
        target_pages = int(request.form.get('target_pages', 8))
        
        # Parse uploaded files
        uploaded_figs = []
        for file in request.files.getlist('figures'):
            if file.filename:
                uploaded_figs.append({'filename': file.filename, 'data': file.read()})

        # 1. Literature
        search_query = f"{title} {keywords}"
        real_bibtex, arxiv_summaries = fetch_arxiv_literature(search_query, max_results=6)
        combined_bibtex = f"{bibtex_entries}\n\n{real_bibtex}".strip()

        # 2. Charts
        generated_charts_list = []
        for idx in range(1, 3):
            chart_asset = generate_academic_chart(api_key, title, results_data, idx)
            if chart_asset: generated_charts_list.append(chart_asset)

        # 3. Sections
        sections_dict = {}
        compiled_user_notes = f"Abstract: {abstract_goals}\nIntro: {intro_bg}\nMethod: {methodology_notes}\nResults: {results_data}\nExtra: {extra_notes}"
        sec_targets = compute_section_targets(target_pages, two_col)
        
        cite_keys = re.findall(r"@\w+\{(\w+),", combined_bibtex)
        cite_str = ", ".join(cite_keys) if cite_keys else "(none)"

        for tag in SECTION_TAGS:
            history = "PREVIOUS SECTIONS:\n" + "".join([f"---{k}---\n{v[:300]}...\n" for k,v in sections_dict.items()]) if sections_dict else ""
            prompt = textwrap.dedent(f"""
            You are writing ONE section of a research paper.
            TITLE: {title} | SECTION: {tag} | WORDS: ~{sec_targets.get(tag, 300)}
            CONTEXT: {arxiv_summaries} | KEYS: {cite_str}
            NOTES: {compiled_user_notes} | {history}
            Write ONLY the '{tag}' section body in LaTeX prose. No headers.
            """)
            raw_text = call_gemini_with_retry(api_key, prompt)
            sections_dict[tag] = clean_llm_latex_output(raw_text)

        # 4. Render & Package
        fig_list = [{"fn": re.sub(r"[^\w.\-]", "_", f['filename']), "cap": f.get('filename')} for f in uploaded_figs]
        fig_list.extend([{"fn": fn, "cap": "Generated plot"} for fn, _ in generated_charts_list])
        
        latex_src = render_latex(title, authors_list, two_col, keywords, sections_dict, combined_bibtex, fig_list)
        zip_payload = generate_overleaf_zip(latex_src, combined_bibtex, uploaded_figs, generated_charts_list)
        
        wcs = {t: len(v.split()) for t, v in sections_dict.items()}
        total_words = sum(wcs.values())

        return jsonify({
            "success": True,
            "latex_src": latex_src,
            "zip_base64": base64.b64encode(zip_payload).decode('utf-8'),
            "sections": sections_dict,
            "word_counts": wcs,
            "total_words": total_words
        })

    except Exception as e:
        return jsonify({"error": str(e)}), 500

